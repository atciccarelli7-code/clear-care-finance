import { mkdir, writeFile } from "node:fs/promises";
import path from "node:path";
import { createServer } from "vite";
import { repositoryRoot } from "./seo-route-utils.mjs";

const outputPath = path.join(repositoryRoot, "src", "data", "runtimeSeoManifest.ts");
const vite = await createServer({
  root: repositoryRoot,
  mode: "production",
  appType: "custom",
  logLevel: "error",
  server: { middlewareMode: true },
});

const assertUnique = (items, label) => {
  const slugs = items.map((item) => item.slug);
  const duplicates = slugs.filter((slug, index) => slugs.indexOf(slug) !== index);
  if (duplicates.length) throw new Error(`Duplicate ${label} SEO slugs: ${Array.from(new Set(duplicates)).join(", ")}`);
};

try {
  const [{ ALL_ARTICLES }, { TOPICS }] = await Promise.all([
    vite.ssrLoadModule("/src/data/allArticles.ts"),
    vite.ssrLoadModule("/src/data/topics.ts"),
  ]);

  const articles = ALL_ARTICLES.map((article) => ({
    slug: article.slug,
    title: article.title,
    description: article.description ?? article.promise,
    category: article.category,
    ...(article.publishedAt ? { publishedAt: article.publishedAt } : {}),
    ...(article.lastReviewedAt ? { lastReviewedAt: article.lastReviewedAt } : {}),
  }));
  const topics = TOPICS.map((topic) => ({
    slug: topic.slug,
    title: topic.title,
    description: topic.promise,
  }));

  assertUnique(articles, "article");
  assertUnique(topics, "topic");

  const source = `// Generated by scripts/generate-runtime-seo-manifest.mjs. Do not edit by hand.\n\n` +
    `export type RuntimeArticleSeoMeta = {\n  slug: string;\n  title: string;\n  description: string;\n  category: string;\n  publishedAt?: string;\n  lastReviewedAt?: string;\n};\n\n` +
    `export type RuntimeTopicSeoMeta = {\n  slug: string;\n  title: string;\n  description: string;\n};\n\n` +
    `export const RUNTIME_ARTICLE_SEO_META = ${JSON.stringify(articles, null, 2)} satisfies RuntimeArticleSeoMeta[];\n\n` +
    `export const RUNTIME_TOPIC_SEO_META = ${JSON.stringify(topics, null, 2)} satisfies RuntimeTopicSeoMeta[];\n`;

  await mkdir(path.dirname(outputPath), { recursive: true });
  await writeFile(outputPath, source, "utf8");
  console.log(`Generated lightweight runtime SEO metadata for ${articles.length} articles and ${topics.length} topics.`);
} finally {
  await vite.close();
}
